package cn.chenlc.qcloud.sdk.vod;

/**
 * 命名参数
 * 
 * @author chenlc
 * @version 1.0
 * @since 2017/4/18
 */
public class NamedParamPair {
    
    private final String key;
    
    private final String value;

    NamedParamPair(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
